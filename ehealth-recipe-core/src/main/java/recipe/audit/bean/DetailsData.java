package recipe.audit.bean;

import java.util.List;

/**
 * Created by jiangtingfeng on 2017/11/2.
 * @author jiangtingfeng
 */
public class DetailsData {
    /**
     * HIS系统时间
     */
    private String time;

    /**
     * 门诊住院标识
     */
    private String hospFlag;

    /**
     * 就诊类型
     */
    private String admType;

    /**
     * 就诊号
     */
    private String admNo;

    /**
     * 检验就诊号
     */
    private String lisAdmNo;

    /**
     * 床位号
     */
    private String bedNo;

    /**
     * 病区
     */
    private String areaCode;

    /**
     * 病人信息
     */
    private AuditPatient patient;

    /**
     * 处方信息
     */
    private List<AuditPrescription> prescriptions;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getHospFlag() {
        return hospFlag;
    }

    public void setHospFlag(String hospFlag) {
        this.hospFlag = hospFlag;
    }

    public String getAdmType() {
        return admType;
    }

    public void setAdmType(String admType) {
        this.admType = admType;
    }

    public String getAdmNo() {
        return admNo;
    }

    public void setAdmNo(String admNo) {
        this.admNo = admNo;
    }

    public String getLisAdmNo() {
        return lisAdmNo;
    }

    public void setLisAdmNo(String lisAdmNo) {
        this.lisAdmNo = lisAdmNo;
    }

    public String getBedNo() {
        return bedNo;
    }

    public void setBedNo(String bedNo) {
        this.bedNo = bedNo;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public AuditPatient getPatient() {
        return patient;
    }

    public void setPatient(AuditPatient patient) {
        this.patient = patient;
    }

    public List<AuditPrescription> getPrescriptions() {
        return prescriptions;
    }

    public void setPrescriptions(List<AuditPrescription> prescriptions) {
        this.prescriptions = prescriptions;
    }
}

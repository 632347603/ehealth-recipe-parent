package recipe.prescription.bean;

import java.util.List;

/**
 * 病人信息
 * Created by jiangtingfeng on 2017/11/2.
 */
public class AuditPatient {
    // 病人姓名
    private String name;

    // 生日
    private String birthDate;

    // 性別
    private String gender;

    // 体重
    private String weight;

    // 身高
    private String height;

    // 身份证号
    private String idCard;

    // 病历卡号
    private String mrCard;

    // 卡类型
    private String cardType;

    // 卡号
    private String cardNo;

    // 怀孕时间
    private String pTime;

    // 怀孕单位
    private String pUnit;

    // 是否为婴儿
    private String isInfant;

    // 过敏信息
    private List<AuditAllergy> allergies;

    // 诊断信息
    private List<AuditDiagnose> diagnoses;

    // 检查单信息
    private List<AuditLisForm> lisForms;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public String getMrCard() {
        return mrCard;
    }

    public void setMrCard(String mrCard) {
        this.mrCard = mrCard;
    }

    public String getCardType() {
        return cardType;
    }

    public void setCardType(String cardType) {
        this.cardType = cardType;
    }

    public String getCardNo() {
        return cardNo;
    }

    public void setCardNo(String cardNo) {
        this.cardNo = cardNo;
    }

    public String getpTime() {
        return pTime;
    }

    public void setpTime(String pTime) {
        this.pTime = pTime;
    }

    public String getpUnit() {
        return pUnit;
    }

    public void setpUnit(String pUnit) {
        this.pUnit = pUnit;
    }

    public String getIsInfant() {
        return isInfant;
    }

    public void setIsInfant(String isInfant) {
        this.isInfant = isInfant;
    }

    public List<AuditAllergy> getAllergies() {
        return allergies;
    }

    public void setAllergies(List<AuditAllergy> allergies) {
        this.allergies = allergies;
    }

    public List<AuditDiagnose> getDiagnoses() {
        return diagnoses;
    }

    public void setDiagnoses(List<AuditDiagnose> diagnoses) {
        this.diagnoses = diagnoses;
    }

    public List<AuditLisForm> getLisForms() {
        return lisForms;
    }

    public void setLisForms(List<AuditLisForm> lisForms) {
        this.lisForms = lisForms;
    }
}
